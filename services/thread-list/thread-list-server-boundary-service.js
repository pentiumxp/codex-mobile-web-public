"use strict";

const { createThreadListFallbackSourceService } = require("./thread-list-fallback-source-service");
const { createThreadListRuntimeService } = require("./thread-list-runtime-service");

function createThreadListServerBoundaryService(options = {}) {
  const fallbackSourceService = createThreadListFallbackSourceService({
    codexHome: options.codexHome,
    sessionsDir: options.sessionsDir,
    rolloutActiveStatusWindowMs: options.rolloutActiveStatusWindowMs,
    staleContextOnlyActiveTurnMs: options.staleContextOnlyActiveTurnMs,
    incrementBoundedDiagnosticCounter: options.incrementBoundedDiagnosticCounter,
    parseJsonLine: options.parseJsonLine,
    readRolloutTail: options.readRolloutTail,
    rolloutEntryTurnId: options.rolloutEntryTurnId,
    timestampToMs: options.timestampToMs,
    statusText: options.statusText,
    statusTurnId: options.statusTurnId,
    isThreadListLiveStatus: options.isThreadListLiveStatus,
    isLiveTurn: options.isLiveTurn,
    rolloutPathForThread: options.rolloutPathForThread,
    readStateDbThread: options.readStateDbThread,
    rowToFallbackThread: options.rowToFallbackThread,
    filterFallbackThreads: options.filterFallbackThreads,
    archivedSessionThreadIds: options.archivedSessionThreadIds,
    collectRecentRolloutFiles: options.collectRecentRolloutFiles,
    threadIdFromRolloutPath: options.threadIdFromRolloutPath,
    isBackupRolloutPath: options.isBackupRolloutPath,
    readGlobalState: options.readGlobalState,
    visibleProjectlessThreadIds: options.visibleProjectlessThreadIds,
    upsertThreadListFallbackCacheThread: options.upsertThreadListFallbackCacheThread,
    applySessionIndexTitleToThread: options.applySessionIndexTitleToThread,
  });

  const runtimeService = createThreadListRuntimeService({
    fallbackCache: options.fallbackCache,
    prewarm: options.prewarm,
    archivedSessionThreadIds: options.archivedSessionThreadIds,
    filterFallbackThreads: options.filterFallbackThreads,
    hydrateThreadListTitlesFromSessionIndex: options.hydrateThreadListTitlesFromSessionIndex,
    isSubagentThreadSummary: options.isSubagentThreadSummary,
    mergeThreadDisplaySummary: options.mergeThreadDisplaySummary,
    mergeThreadWithCachedDisplaySummary: options.mergeThreadWithCachedDisplaySummary,
    normalizeFsPath: options.normalizeFsPath,
    normalizeThreadId: options.normalizeThreadId,
    normalizeThreadSummaryLiveStatus: options.normalizeThreadSummaryLiveStatus,
    readGlobalState: options.readGlobalState,
    readRolloutSessionFallback: (...args) => fallbackSourceService.readRolloutSessionFallback(...args),
    readSessionIndexFallback: (...args) => fallbackSourceService.readSessionIndexFallback(...args),
    readStateDbFallback: options.readStateDbFallback,
    scheduleActiveWindowPrewarmFromThreadListResult: options.scheduleActiveWindowPrewarmFromThreadListResult,
    shouldHideThreadListSummary: options.shouldHideThreadListSummary,
    stripThreadListDetailFields: options.stripThreadListDetailFields,
    stripThreadListResultDetailFields: options.stripThreadListResultDetailFields,
    threadHasArchiveSignal: options.threadHasArchiveSignal,
    timestampToMs: options.timestampToMs,
    visibleProjectlessThreadIds: options.visibleProjectlessThreadIds,
    visibleWorkspaceRoots: options.visibleWorkspaceRoots,
    logger: options.logger,
  });

  return {
    threadListFallbackSourceService: fallbackSourceService,
    threadListRuntimeService: runtimeService,
    threadListResponseCoalescer: runtimeService.threadListResponseCoalescer,

    attachRolloutFallbackStatus: fallbackSourceService.attachRolloutFallbackStatus,
    fallbackDisplayText: fallbackSourceService.fallbackDisplayText,
    hydrateThreadTitleFromSessionIndex: fallbackSourceService.hydrateThreadTitleFromSessionIndex,
    inferRolloutFallbackStatus: fallbackSourceService.inferRolloutFallbackStatus,
    isRolloutTerminalEntry: fallbackSourceService.isRolloutTerminalEntry,
    normalizeStaleContextOnlyActiveThread: fallbackSourceService.normalizeStaleContextOnlyActiveThread,
    persistThreadTitleToSessionIndex: fallbackSourceService.persistThreadTitleToSessionIndex,
    readRolloutHead: fallbackSourceService.readRolloutHead,
    readRolloutSessionFallback: fallbackSourceService.readRolloutSessionFallback,
    readRolloutSessionFallbackThread: fallbackSourceService.readRolloutSessionFallbackThread,
    readRolloutSessionFallbackThreadFromFile: fallbackSourceService.readRolloutSessionFallbackThreadFromFile,
    readSessionIndexEntries: fallbackSourceService.readSessionIndexEntries,
    readSessionIndexEntriesForFallback: fallbackSourceService.readSessionIndexEntriesForFallback,
    readSessionIndexFallback: fallbackSourceService.readSessionIndexFallback,
    rolloutLatestTurnEvidence: fallbackSourceService.rolloutLatestTurnEvidence,
    staleContextOnlyActiveEvidenceForRollout: fallbackSourceService.staleContextOnlyActiveEvidenceForRollout,
    staleContextOnlyActiveStatus: fallbackSourceService.staleContextOnlyActiveStatus,

    mergeThreadSummaryListWithDiagnostics: runtimeService.mergeThreadSummaryListWithDiagnostics,
    mergeThreadSummaryList: runtimeService.mergeThreadSummaryList,
    mergeThreadListFallback: runtimeService.mergeThreadListFallback,
    normalizeThreadListResultStatuses: runtimeService.normalizeThreadListResultStatuses,
    threadListSummaryTimestampMs: runtimeService.threadListSummaryTimestampMs,
    sortThreadListSummaries: runtimeService.sortThreadListSummaries,
    clearThreadListFallbackCache: runtimeService.clearThreadListFallbackCache,
    removeThreadFromThreadListFallbackCache: runtimeService.removeThreadFromThreadListFallbackCache,
    upsertThreadListFallbackCacheThread: runtimeService.upsertThreadListFallbackCacheThread,
    updateThreadListFallbackCacheStatus: runtimeService.updateThreadListFallbackCacheStatus,
    applyThreadStatusPayloadToThreadListFallbackCache: runtimeService.applyThreadStatusPayloadToThreadListFallbackCache,
    trackThreadDetailRequestLifecycle: runtimeService.trackThreadDetailRequestLifecycle,
    shouldDeferThreadListFallbackForActiveDetail: runtimeService.shouldDeferThreadListFallbackForActiveDetail,
    threadListFallbackCacheKey: runtimeService.threadListFallbackCacheKey,
    rememberThreadListFallbackCache: runtimeService.rememberThreadListFallbackCache,
    readThreadListFallbackCache: runtimeService.readThreadListFallbackCache,
    readThreadListCachedFallback: runtimeService.readThreadListCachedFallback,
    readThreadListFallback: runtimeService.readThreadListFallback,
    threadListFallbackPrewarmConfig: runtimeService.threadListFallbackPrewarmConfig,
    threadListFallbackPrewarmPublicStatus: runtimeService.threadListFallbackPrewarmPublicStatus,
    scheduleThreadListFallbackPrewarm: runtimeService.scheduleThreadListFallbackPrewarm,
    threadListFallbackSourceDiagnosticTimingFields: runtimeService.threadListFallbackSourceDiagnosticTimingFields,
    threadListFallbackBaselineWorkTimingFields: runtimeService.threadListFallbackBaselineWorkTimingFields,
    threadListTokenUsageTimingFields: runtimeService.threadListTokenUsageTimingFields,
  };
}

module.exports = {
  createThreadListServerBoundaryService,
};
